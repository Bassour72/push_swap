#include "ft_printf.h"

int	ft_isspecifiertype(char c)
{
	return (c == '%' || c == 'c' || c == 's' || c == 'i' || \
	c == 'd' || c == 'p' || c == 'x' || c == 'X' || c == 'u');
}
